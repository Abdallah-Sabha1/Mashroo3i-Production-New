        private void AddSectionHeader(Document doc, string title, Font font)
        {
            doc.Add(new Paragraph(title, font) { SpacingAfter = 15 });
            doc.Add(new Paragraph(new Chunk(new iTextSharp.text.pdf.draw.LineSeparator(1f, 100f, LightGray, Element.ALIGN_LEFT, -1))));
            doc.Add(new Paragraph("\n"));
        }

        private void AddTableRow(PdfPTable table, string label, string value, Font labelFont, Font valueFont)
        {
            table.AddCell(new PdfPCell(new Phrase(label, labelFont))
                { Padding = 8, BackgroundColor = LightGray, BorderColor = BaseColor.White });
            table.AddCell(new PdfPCell(new Phrase(value, valueFont))
                { Padding = 8, BorderColor = BaseColor.White });
        }

        private void AddSwotSection(Document doc, string title, JsonElement root, string key, BaseColor bgColor, Font headFont, Font bodyFont)
        {
            if (root.TryGetProperty(key, out var items) && items.ValueKind == JsonValueKind.Array)
            {
                var cell = new PdfPTable(1) { WidthPercentage = 100, SpacingBefore = 8 };
                cell.AddCell(new PdfPCell(new Phrase(title, headFont))
                    { BackgroundColor = bgColor, Padding = 10, BorderColor = BaseColor.White });

                var list = new List(List.UNORDERED);
                list.SetListSymbol("\u2022  ");
                foreach (var item in items.EnumerateArray())
                    list.Add(new ListItem(item.GetString() ?? "", bodyFont));

                var contentCell = new PdfPCell { Padding = 10, BorderColor = BaseColor.White };
                contentCell.AddElement(list);
                cell.AddCell(contentCell);
                doc.Add(cell);
            }
        }

        private static List<string> ParseStringArray(string? json)
        {
            if (string.IsNullOrEmpty(json)) return new();
            try
            {
                if (json.TrimStart().StartsWith('['))
                    return JsonSerializer.Deserialize<List<string>>(json) ?? new();
                return new List<string> { json };
            }
            catch { return new(); }
        }

        private static List<string> ParseFinancialArray(string? summaryJson, string arrayKey)
        {
            if (string.IsNullOrEmpty(summaryJson)) return new();
            try
            {
                var doc  = JsonDocument.Parse(summaryJson).RootElement;
                if (doc.TryGetProperty(arrayKey, out var arr) && arr.ValueKind == JsonValueKind.Array)
                    return arr.EnumerateArray().Select(e => e.GetString() ?? "").Where(s => s.Length > 0).ToList();
            }
            catch { }
            return new();
        }

        private record ScenarioValues(decimal revenue, decimal costs, decimal profit, int bep);

        private static ScenarioValues? GetScenario(string? summaryJson, string key)
        {
            if (string.IsNullOrEmpty(summaryJson)) return null;
            try
            {
                var root = JsonDocument.Parse(summaryJson).RootElement;
                if (!root.TryGetProperty(key, out var s)) return null;
                return new ScenarioValues(
                    GetDecimal(s, "monthlyRevenue"),
                    GetDecimal(s, "monthlyCosts"),
                    GetDecimal(s, "monthlyProfit"),
                    GetInt(s, "breakEvenMonths"));
            }
            catch { return null; }
        }

        private record ProjectionRow(int month, decimal revenue, decimal costs);

        private static List<ProjectionRow> GetProjections(string? summaryJson)
        {
            if (string.IsNullOrEmpty(summaryJson)) return new();
            try
            {
                var root = JsonDocument.Parse(summaryJson).RootElement;
                if (!root.TryGetProperty("monthlyProjections", out var arr) || arr.ValueKind != JsonValueKind.Array)
                    return new();
                return arr.EnumerateArray().Select(p => new ProjectionRow(
                    GetInt(p, "month"),
                    GetDecimal(p, "revenue"),
                    GetDecimal(p, "costs")
                )).ToList();
            }
            catch { return new(); }
        }

        private record BenchmarkRow(string metric, string yourValue, string benchmark, string status);

        private static List<BenchmarkRow> GetBenchmarks(string? summaryJson)
        {
            if (string.IsNullOrEmpty(summaryJson)) return new();
            try
            {
                var root = JsonDocument.Parse(summaryJson).RootElement;
                if (!root.TryGetProperty("benchmarkComparisons", out var arr) || arr.ValueKind != JsonValueKind.Array)
                    return new();
                return arr.EnumerateArray().Select(b => new BenchmarkRow(
                    GetString(b, "metric"),
                    GetString(b, "yourValue"),
                    GetString(b, "benchmarkTypical"),
                    GetString(b, "status")
                )).ToList();
            }
            catch { return new(); }
        }

        private static decimal GetDecimal(JsonElement el, string key) =>
            el.TryGetProperty(key, out var v) && v.TryGetDecimal(out var d) ? d : 0m;

        private static int GetInt(JsonElement el, string key) =>
            el.TryGetProperty(key, out var v) && v.TryGetInt32(out var i) ? i : 0;

        private static string GetString(JsonElement el, string key) =>
            el.TryGetProperty(key, out var v) ? v.GetString() ?? "" : "";

